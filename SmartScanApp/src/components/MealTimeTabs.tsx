// ── Map meal name → icon component ──
const mealIcons: Record<string, React.FC<{ color: string }>> = {
  Breakfast: BreakfastIcon,
  Lunch: LunchIcon,
  Dinner: DinnerIcon,
  Snack: SnackIcon,
};

// ════════════════════════════════════════════════════════
// MealTimeTabs — the exported reusable component
// ════════════════════════════════════════════════════════

export default function MealTimeTabs({ selectedMeal, onSelectMeal }: Props) {
  return (
    <View style={styles.container}>
      {meals.map((meal, index) => {
        const isSelected = selectedMeal === meal;
        const isFirst = index === 0;
        const isLast = index === meals.length - 1;
        const IconComponent = mealIcons[meal];

        // Figma: all tabs use #031aa1 icon + text color
        const iconColor = '#031aa1';

        return (
          <TouchableOpacity
            key={meal}
            style={[
              styles.tab,
              isSelected && styles.tabSelected,
              isFirst && styles.tabFirst,
              isLast && styles.tabLast,
              // Inner divider borders: 0.5px between tabs
              !isFirst && styles.tabInnerLeft,
              !isLast && styles.tabInnerRight,
            ]}
            onPress={() => onSelectMeal(meal)}
            activeOpacity={0.7}
          >
            <IconComponent color={iconColor} />
            <Text style={styles.tabLabel}>{meal}</Text>
          </TouchableOpacity>
        );
      })}
    </View>
  );
}

